class Solution {
  constructor() {}

  /**
   * @param {number} width
   * @param {number} height
   * @return {number}
   */
  RectangleArea(width, height) {
    // Your code goes here
    return width * height;
  }

  /**
   * @param {number} radius
   * @return {number}
   */
  CircleArea(radius) {
    // Your code goes here
    return Math.PI * radius * radius;
  }
}
